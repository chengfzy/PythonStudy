def draw_april_tag(c: pyx.canvas, april_tag, tag_id: int, position, tag_size: float, black_border=2, tag_space=0):
    """
    Draw AprilTag in canvas
    :param c: Canvas
    :param april_tag: AprilTag info
    :param tag_id: Tag ID
    :param position: The origin(bottom-left) position to draw the tag
    :param tag_size: Tag size [cm]
    :param black_border: Black border size
    :param tag_space: Space size between tags [cm]. If the value <= 0, then don't draw the space block
    """
    # get the tag code
    if tag_id < 0 or tag_id >= len(april_tag.tag_code):
        print(
            f'Request tag ID = {tag_id} is out of range [{0}, {len(april_tag.tag_code)}) '
            f'in the tag family {april_tag.chosen_tag_family}'
        )
    tag_code = april_tag.tag_code[tag_id]

    # calculate the bit size of the tag
    sqrt_bits = math.sqrt(april_tag.total_bits)
    bit_square_size = tag_size / (sqrt_bits + black_border * 2)  # size for each bit square

    # position of tag
    x_pos = position[0]
    y_pos = position[1]

    # draw border
    border_size = black_border * bit_square_size
    c.fill(pyx.path.rect(x_pos, y_pos, tag_size, border_size), [pyx.color.rgb.black])  # bottom
    c.fill(pyx.path.rect(x_pos, y_pos + tag_size - border_size, tag_size, border_size), [pyx.color.rgb.black])  # top
    c.fill(pyx.path.rect(x_pos + tag_size - border_size, y_pos, border_size, tag_size), [pyx.color.rgb.black])  # left
    c.fill(pyx.path.rect(x_pos, y_pos, border_size, tag_size), [pyx.color.rgb.black])  # right

    # create numpy matrix of code
    code_matrix = np.zeros((int(sqrt_bits), int(sqrt_bits)))
    for i in range(int(sqrt_bits)):
        for j in range(int(sqrt_bits)):
            if not tag_code & (1 << int(sqrt_bits) * i + j):
                code_matrix[i, j] = 1
    # flip, rotation 180 deg
    code_matrix = np.rot90(code_matrix, 2)

    # draw tag bits
    for i in range(int(sqrt_bits)):
        for j in range(int(sqrt_bits)):
            if code_matrix[i, j]:
                c.fill(
                    pyx.path.rect(
                        x_pos + (j + black_border) * bit_square_size,
                        y_pos + ((black_border - 1) + sqrt_bits - i) * bit_square_size,
                        bit_square_size,
                        bit_square_size,
                    ),
                    [pyx.color.rgb.black],
                )

    # draw tag space (decreases the effect of motion blur in the subpix refinement)
    if tag_space > 0:
        corners = [
            [x_pos - tag_space, y_pos - tag_space],
            [x_pos + tag_size, y_pos - tag_space],
            [x_pos + tag_size, y_pos + tag_size],
            [x_pos - tag_space, y_pos + tag_size],
        ]
        for point in corners:
            c.fill(pyx.path.rect(point[0], point[1], tag_space, tag_space), [pyx.color.rgb.black])


def generate_april_target(c: pyx.canvas, rows, cols, tag_size, tag_space, tag_family='t36h11', start_id=0):
    """
    Generate April Target
    :param c: Canvas
    :param rows: Rows number of tags
    :param cols: Cols number of tags
    :param tag_size: Size of each tag [m]
    :param tag_space: Space size between tags [m]
    :param tag_family: Tag family
    :param start_id: The start(min, origin) ID of the target
    """
    # convert to cm
    tag_size = tag_size * 100.0
    tag_space = tag_space * 100

    # get the tag family data
    april_tag = AprilTag(tag_family)

    # draw tags
    for y in range(rows):
        for x in range(cols):
            tag_id = cols * y + x + start_id
            pos = (x * (tag_size + tag_space), y * (tag_size + tag_space))
            draw_april_tag(c, april_tag, tag_id, pos, tag_size, black_border=1, tag_space=tag_space)

    # draw axis
    pos = (-1.5 * tag_space, -1.5 * tag_space)
    c.stroke(
        pyx.path.line(pos[0], pos[1], pos[0] + tag_size * 0.3, pos[1]),
        [
            pyx.color.rgb.red,
            pyx.deco.earrow(
                [pyx.deco.stroked([pyx.color.rgb.red, pyx.style.linejoin.round]), pyx.deco.filled([pyx.color.rgb.red])],
                size=tag_size * 0.10,
            ),
        ],
    )

    c.text(pos[0] + tag_size * 0.32, pos[1] - tag_size * 0.01, "x")
    c.stroke(
        pyx.path.line(pos[0], pos[1], pos[0], pos[1] + tag_size * 0.3),
        [
            pyx.color.rgb.green,
            pyx.deco.earrow(
                [
                    pyx.deco.stroked([pyx.color.rgb.green, pyx.style.linejoin.round]),
                    pyx.deco.filled([pyx.color.rgb.green]),
                ],
                size=tag_size * 0.10,
            ),
        ],
    )
    c.text(pos[0] - tag_size * 0.01, pos[1] + tag_size * 0.32, "y")
    # target info
    c.text(
        pos[0] + 0.6 * tag_size,
        pos[1] - tag_size * 0.01,
        f'{rows}x{cols}, start ID = {start_id}, size = {tag_size:.2f}cm, space ={tag_space:.2f}cm',
    )


